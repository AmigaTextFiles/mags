<p><font size="4"> <B>Bonner
Staatsanwaltschaft ermittelt gegen Ron Sommer</B></FONT><br><font
size="-1"><B>Telekom-Vorstand
wusste frühzeitig von der Falschbewertung der Immobilien</B></font><p><font
size="-1">Bonn (pte, 11. Mar
2001 09:15) - Die Bonner Staatsanwaltschaft hat gegen Telekom-Chef Ron
Sommer <A HREF="http://www.dtag.de" target="new">http://www.dtag.de</A> ein
förmliches Ermittlungsverfahren aufgenommen. Nach Informationen des
Nachrichtenmagazins "Der Spiegel" vermutet die Staatsanwaltschaft, Sommer
könnte schon seit langem von der Überbewertung der Telekom-Immobilien
gewusst haben, ohne die Bilanz entsprechend zu korrigieren.<br><br>Laut
"Spiegel" liegen den Ermittlern Schreiben vor, in denen Telekom-Manager und
externe Gutachter den ehemaligen Aufsichtsratsvorsitzenden Helmut Sihler
und den Telekom-Vorstand bereits frühzeitig auf eine drohende
Falschbewertung hingewiesen haben. Die Telekom weist die Vorwürfe jedoch
als unbegründet zurück.<br><br>Bislang stand in erster Linie der ehemalige
Telekom-Finanzvorstand Joachim Kröske bei den Ermittlern wegen
Falschbilanzierung und Kapitalbetrugs unter Verdacht. Ein von der Bonner
Staatsanwaltschaft angefordertes Gutachten ergab, dass die Immobilien viel
zu hoch bewertet worden waren. Die Telekom hatte ihre Bilanz daraufhin um
3,9 Mrd. Mark nach unten korrigiert. (Ende)<p> <BR clear=all> <A
