<font color="#000080" size="7"><b>Das kleine ...zig!</b></font>
<br><br>
<font size=+1>
Das kleine ...zig ist ein Fanal: <br>
mit "20" kommt's zum ersten Mal! <br>
Du fändest dieses ...zig noch fein <br>
und möchtest eher älter sein. <br>
<br>
Mit "30" machts du dir nichts draus: <br>
Du kennst dich damit ja schon aus, <br>
bist immer fleißig - denke ich, <br>
bis es erscheint, das nächste ...zig. <br>
<br>
Mit "40" wird man sich besinnen, <br>
man schaut dann auch schon mal nach innen. <br>
Und bei dem Blick zurück <br>
ergibt sich: man hatte Glück. <br>
<br>
Mit "50" kommt's als Donnerhall <br>
dir vor, das kleine zig-Signal, <br>
du findest's gar nicht mehr so schön <br>
und denkst: "Wie wird's wohl weitergehn?" <br>
<br>
Doch kaum versiehst du dich <br>
macht es schon wieder einmal ...zig. <br>
Nun bist du aber recht perplex, <br>
denn vor dem ...zig steht sie, die Sechs. <br>
<br>
Noch bist du jung, viel Glück für dich, <br>
später kommt das nächste ...zig. <br>
"70" sind es somit heut', <br>
Du hast bestimmt kein Jahr bereut. <br>
<br>
Bist Du erst "100", ist's famos. <br>
Du bist das ...zig fürs erste los.

